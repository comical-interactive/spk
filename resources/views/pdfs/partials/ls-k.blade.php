<h3 class="text-center mt-3">Penjelasan</h3>

<div>
    <h4>Gaya Belajar Kinestetik</h4>

    <p>Anak dengan gaya belajar kinestetik dapat belajar dengan cara bergerak, bekerja dan menyentuh. Mereka
    mengandalkan indera peraba, yakni “tangan” sebagai alat utama untuk bisa menyerap, memahami dan mengingat suatu
    informasi atau pengetahuan. Artinya, mereka harus memegang objek tertentu untuk dapat menyerap dan memahami
    informasi itu.</p>
</div>

<div>
    <h4>Ciri-ciri gaya belajar kinestetik :</h4>
    <ul>
        <li>Sulit berdiam diri atau duduk manis, selalu ingin bergerak</li>
        <li>Mengerjakan segala sesuatu yang memungkinkan tangannya aktif. Contoh: saat guru menerangkan pelajaran, dia
        mendengarkan sambil tangannya asyik menggambar</li>
        <li>Biasanya sulit menguasai hal-hal abstrak seperti peta, symbol dan lambang</li>
        <li>Suka menggunakan objek nyata sebagai alat bantu belajar (praktek atau percobaan)</li>
        <li>Menyukai permainan dan aktivitas fisik</li>
        <li>Menggunakan gerakan tubuh untuk menjelaskan sesuatu</li>
        <li>Menggunakan jari sebagai penunjuk ketika membaca</li>
        <li>Biasanya tidak memiliki tulisan tangan yang bagus</li>
    </ul>
</div>

<div>
    <h4>Strategi belajar :</h4>
    <ul>
        <li>Anak tipe ini akan antusias apabila diajak bereksperimen atau praktek langsung. Karena mereka dapat dengan
        mudah memahami materi dengan “belajar dari pengalaman” (Experiential learning) dan menggunakan alat. sehingga
        anak dapat menyentuh langsung, bukan hanya membayangi konsep secara abstrak saja</li>
        <li>Karena menggunakan otot-otot motorik besar ketika belajar, oleh karena itu dalam menghapal pelajaran anak
        akan berjalan mondar-mandir dan melihat-lihat</li>
        <li>Ajak anak bermain peran/ role playing atau simulasi tentang materi yang dipelajari</li>
        <li>Ajak anak kunjungan lapangan dan wisata untuk melihat objek yang sedang dibahas</li>
        <li>Saat belajar diiringi musik yang disukai anak dengan suasana yang santai, tidak tegang</li>
        <li>Lebih berpartisipasi aktif daripada hanya menonton video atau membaca dari buku</li>
    </ul>
</div>