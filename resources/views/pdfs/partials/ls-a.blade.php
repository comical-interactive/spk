<h3>Penjelasan</h3>

<div>
    <h4>Gaya Belajar Auditori</h4>

    <p>Anak dengan gaya belajar auditori mengandalkan “pendengaran” sebagai alat utama untuk bisa menyerap, memahami
    dan mengingat suatu informasi atau pengetahuan. Artinya, mereka harus mendengar, baru kemudian bisa mengingat dan
    memahami informasi itu. Mereka sulit untuk menyerap informasi dalam bentuk tulisan secara langsung atau lewat
    membaca.</p>
</div>

<div>
    <h4>Ciri-ciri gaya belajar Auditori :</h4>
    <ul>
        <li>Mampu mengingat dengan baik penjelasan lisan, misalnya penjelasan guru di depan kelas</li>
        <li>Senang berdiskusi dan berkomunikasi dengan orang lain</li>
        <li>Pandai berbicara, lebih suka bercerita daripada menulis</li>
        <li>Kurang terampil dalam mengerjakan tugas mengarang/menulis</li>
        <li>Pendengar yang baik. Anak mudah menguasai materi/jingle iklan, suka mendengarkan musik</li>
        <li>Kurang suka membaca dan umumnya memang bukan pembaca yang baik karena kurang dapat mengingat dengan baik
        apa yang baru saja dibacanya</li>
        <li>Mudah terganggu dengan suara lain saat belajar</li>
    </ul>
</div>

<div>
    <h4>Strategi belajar :</h4>

    <ul>
        <li>Dalam menghafal bisa sambil dilagukan</li>
        <li>Rekam materi pembelajaran dalam kaset dan dengarkan sebelum tidur</li>
        <li>Belajar akan lebih efektif dengan Ikut berpartisipasi dalam diskusi kelompok, mendengarkan ceramah, buku
        audio, presentasi lisan, petunjuk lisan</li>
        <li>Bila catatan tidak memadai, dapat ditambahkan informasi dari hasil diskusi dengan orang lain</li>
        <li>Dalam membaca, akan lebih mudah memahami bacaannya jika membacanya sambil mengeluarkan suara</li>
        <li>Anak akan lebih cepat paham suatu topik dengan cara ia menjelaskannya kembali secara lisan dan ayah bunda
        membantu untuk mendengarkan penjelasan si anak</li>
        <li>Ciptakan suasana belajar yang kondusif, sunyi dan tenang</li>
        <li>Menggunakan asosiasi kata untuk mengingat fakta-fakta dan garis</li>
    </ul>
</div>