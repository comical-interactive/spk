<h3>Penjelasan</h3>

<div>
    <h4>Gaya Belajar Visual</h4>

    <p>Anak dengan gaya belajar visual mengandalkan indera “penglihatan” sebagai alat utama untuk bisa menyerap,
    memahami dan mengingat suatu informasi atau pengetahuan. Artinya, mereka harus melihat, baru kemudian bisa
    mengingat dan memahami informasi itu. Mereka lebih suka membaca, memperhatikan ilustrasi yang ditempel, gambar
    warna warni, diagram, grafik dan senang membuat catatan.</p>
</div>

<div>
    <h4>Ciri-ciri gaya belajar visual :</h4>

    <ul>
        <li>Mudah mengingat penjelasan jika menggunakan gambar, diagram, atau video</li>
        <li>Suka membuat coretan saat berpikir dan mencatat</li>
        <li>Suka membuat catatan sedetail-detailnya agar mendapatkan informasi yang lengkap</li>
        <li>Cepat dalam memahami bacaan, lebih suka membaca dari pada dibacakan</li>
        <li>Perhatian mudah teralih jika melihat sesuatu yang lebih menarik.</li>
        <li>Tidak begitu banyak bicara, kegiatannya lebih banyak mengerjakan hal-hal yang berkaitan dengan abstraksi
        ruang seperti: mencoret-coret, mewarnai, bermain puzzel, menyusun balok, dan lain-lain</li>
        <li>Sangat senang mengukur sesuatu dengan alat sederhana yang ditemukannya atau dengan anggota tubuhnya sendiri
        seperti: menjengkal atau melangkah, mengukur panjang atau pendek, besar kecil, jauh atau dekat, dsb</li>
        <li>Rapi, teratur, teliti terhadap hal-hal detail, memperhatikan segala sesuatu dan menjaga penampilan.</li>
    </ul>
</div>

<div>
    <h4>Strategi belajar :</h4>

    <ul>
        <li>Ajak anak membaca buku-buku berilustrasi, grafik atau gambar</li>
        <li>Gunakan multimedia, misalnya nonton film dan browsing berbagai hal yang ingin diketahui berkaitan dengan
        materi pelajarannya. Kemudian ajak anak mencoba mengilustrasikan ide-idenya ke dalam gambar.</li>
        <li>Biasakan membuat catatan yang rapi dan menarik agar Lebih mudah mengingat bila belajar langsung dari buku
        catatan</li>
        <li>Gunakan highlighter untuk menandai hal-hal yang penting. Bisa menggunakan stabilo, kata penting dilingkari,
        garis bawah dan kode warna kata</li>
        <li>Membuat garis besar dari kalimat dan istilah yang merupakan informasi kunci dari buku pelajaran</li>
        <li>Buatlah situasi belajar yang kondusif, sunyi dan tenang</li>
        <li>Apabila mempelajari informasi yang bersifat angka angka dan rumus, tulislah kembali pemahamannya tentang
        materi tersebut dalam bentuk tulisan</li>
        <li>Untuk menghafal, ayah bunda dapat membantu membuatkan flash cards</li>
        <li>Lebih banyak menitikberatkan pada alat peraga. Ajak mereka ke objek-objek yang berkaitan dengan pelajaran
        tersebut, dengan cara menunjukkan alat peraganya langsung pada anak atau menggambarkannya di papan tulis</li>
    </ul>
</div>
